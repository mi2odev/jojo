export const charactersByKey: Record<CharacterKey, CharacterData> = charactersData.reduce(
  (acc, c) => { acc[c.key] = c; return acc; },
  {} as Record<CharacterKey, CharacterData>,
);

/** Resolve one character's localizable fields into a single language. */
export function localizeCharacter(c: CharacterData, lang: Lang): LocalizedCharacter {
  return {
    key: c.key,
    name: c.name[lang],
    part: c.part,
    partName: c.partName[lang],
    stand: c.stand,
    standType: c.standType[lang],
    title: c.title[lang],
    description: c.description[lang],
    quote: c.quote[lang],
    cry: c.cry[lang],
    traits: c.traits[lang],
    strengths: c.strengths[lang],
    weaknesses: c.weaknesses[lang],
    leadershipStyle: c.leadershipStyle[lang],
    combatStyle: c.combatStyle[lang],
    emotionalProfile: c.emotionalProfile[lang],
    lifePhilosophy: c.lifePhilosophy[lang],
    color: c.color,
    accent: c.accent,
    image: c.image,
    emoji: c.emoji,
    stats: c.stats,
    metrics: c.metrics,
  };
}

export function getCharacters(lang: Lang): Record<CharacterKey, LocalizedCharacter> {
  return charactersData.reduce((acc, c) => {
    acc[c.key] = localizeCharacter(c, lang);
    return acc;
  }, {} as Record<CharacterKey, LocalizedCharacter>);
}

export function getCharacterList(lang: Lang): LocalizedCharacter[] {
  return charactersData.map((c) => localizeCharacter(c, lang));
}
